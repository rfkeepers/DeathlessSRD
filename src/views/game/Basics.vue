<h1 class="title">
    The Basics
</h1>
Deathless is about flawed characters who are never allowed to die.  Without death they have no fear, but also no relief. At the table, we play to find out where those characters' lines are drawn: the boundaries they preserve, and the ones they're willing to step across.
<br>
<hr>

<h2 id="pbta">
    Powered By The Apocalypse
</h2>
Deathless is modeled on the Apocalypse Engine (aka, pbta), which is based on <a href="https://lumpley.games/apocalypseworld/">Apocalpyse World</a> by D. Vincent and Meguey Baker.  If you're familiar with other pbta games (possibly: Masks, Dungeon World, or Monster of the Week), then you probably know what you're in for, more or less.
<br><br>
If you're new to PBTA games, here's what to expect: the experience is light on rules, heavy on the narrative, and has strong opinions about which stories and spaces the game inhabits best.  The setting, worldbuilding, and characters will be your own, while the game provides the atmosphere and tone.

<h2 id="players">
    Players
</h2>
Each player at the table creates a character (a Player Character, or PC)- <em>one of the deathless</em>- whom they control for the lifespan of the story.  A player's job is to act as that charcter: to say the things the character says, describe what the character does, react to the world as that character would; to bring them to life in dramatic fashion.

<h2 id="mc">
    Master of Ceremonies
</h2>
The MC puppets all of <em>the world</em> around the PCs, acting out the Non-Player Characters (aka, NPCs) and their motives and reactions.  Their job, while pulling at the strings, is to maintain the tempo and tension of the game, to react to the PCs actions and, like everyone else, to discover the story as it happens.

<h2 id="play">
    Play
</h2>
To play this game is to have a <em>conversation</em>.
<br><br>
The MC describes something that's happening: an event taking place or the actions of an NPC.  The Players say how their characters respond.  If the PC's behavior needs to be addressed in a particular way (rules, moves, etc), then the MC and Players work it out as needed.  Afterward, the MC says how the world responds, which loops things back to the beginning.
<br><br>
It works the same if the Players hash out a moment together without the MC's input.  One Player describes what their character does or says.  Another PC responds.  Then someone else can respond, and so on.  The MC can interject at any moment to assert rules and other such arbitrations, as needed.
<br><br>
There are no rules about who gets to talk and when, people contribute when they feel like it.  Everyone should have an opportunity to add to the conversation.  The topic should always move forward to new and interesting spaces.  Everyone takes part in directing the content, towards the things they want to explore, and away from the things they don't.
<br><br>
All the things said at the table sum up to create "the fiction".  Since the fiction is the story, that means <em>every person involved is a co-author</em>.  No one person at the table has special agency over the conversation that others do not.  We all play to find out what happens.

<h2 id="moves">
    Moves
</h2>
Every now and then the game gets a say in the conversation, too.  These are the rules speaking up, helping the table resolve a moment when the fictional outcome isn't clear on its own, or when the game wants to steer the play in a certain direction.  This gets handled through (link-needed to anatomy-of-moves)<b>Moves</b>.
<br><br>
Moves are codified fictional action.  They come into play according to a trigger; something like, "When you act with "precision, subtlety, or prior experience".  What this means is when you're having the conversation and Jen says, "Flynn slides his hand into the Baron's pocket, sneaking out the key to his house."  The MC should say, "oh, that sounds like you're trying to Snipe something!  That's a move, let's roll the dice to find out if you do it!"  That's where the game adds to the conversation: you look up the move Snipe, roll the dice, and do as the move prescribes to learn the results.
<br><br>
After that, the conversation turns back to the people at the table.
<br>
<hr>

<h2 id="agendas">
    Agendas
</h2>
Deathless is not designed for all possible stories and games.  It wants to investigate <em>humanity</em>, <em>weakness</em>, and character <em>faults</em>.  It wants to have the players question, "what have I done?", more than they ask, "how do I want to do it?".  The scope is fantastical- but from a ground-level perspective- setting the supernatural as a foil to ourselves to see how that makes us, people, more interesting.  If a character seeks to slay a god, the game doesn't care if they have the power to do it.  It cares about what drives a person to kill their god, and why they will not be received as saviors on their return.
<br><br>

To steer the tone of play toward these stories, everyone at the table has an agenda.  The Players, the MC, secretly even the rules themselves.  These agendas form a contract.  An agreement between all involved that they are here to play the same game.

<div class="spaceAbove">
    <h3 id="player_agenda">
        Player Agendas
    </h3>

    <h4 class="noTopMargin">⇀ Play a real person: flawed, <em>engaged</em>, and aware</h4>
    Chracters should behave like real people.  They're at their best when: Flawed- able to make mistakes and act irrationally or in their own self-interest.  Engaged- seeking earnest and active involvement in what's going on around them.  Aware- thoughtful, introspective, and self-aware, just like the player.

    <h4>⇀ Embrace all consequences</h4>
    Characters in deathless are not superhuman.  The world around them is inhospitable.  Trouble and tragedy are an expected part of play.  If your goal is to avoid trial and failure you'll end up fighting the game itself.  Allow your character to experience hardship.  Dig into that, and explore it.

    <h4>⇀ Appreciate the fragility of good connections</h4>
    Your character cannot die, but that isn't the case for everyone in the world.  On the contrary, you're the exception, and part of living forever means enduring loss.  But loss doesn't make life worth living.  Accept loss, but live to find friendship, hope, love, and dedication.  Hold tight to those things.  Dig your fingers in and make the world pry them from your grip.

    <h4>⇀ Don't be afraid to make different commitments than your fellow players</h4>
    You and your fellow players might all be in this together, but that doesn't mean you'll always share the same outlook on the situation.  When you disagree with the path the others are heading down- or even further, you want try and stop them from walking it- go ahead and chase that conflict.  The rules are ready to handle your quarrels, and you should trust everyone to bring things around again afterward.
</div>

<div class="spaceAbove">
    <h3 id="mc_agenda">
        MC Agendas
    </h3>

    <h4 class="noTopMargin">⇀ Make the world crooked, remorseless, and <em>hungry</em></h4>
    Give your world opportunities to eat people alive.  Actually, do more than that:  don't wait for opportunites to arrive, make the world lunge out and snatch people up.  Make it crooked, willing to cheat and backstab its way to the next bite.  Make it remorseless, indifferent about who it might chew up and spit out and how that choice could affect others, or itself.  Finally, make it hungry, which draws two implications.  First, that your world is always looking for its next meal.  Second, that it isn't at a buffet; it has to work for what it gets, just like the rest of us.

    <h4>⇀ Make the people layered, surprising, and real</h4>
    All people have names, lives, desires and secrets.  Lackeys harbor precious joys.  Leaders expeiences crisis.  Anyone drifting through the background has an ambition.  Importantly, it is not your job to write backstories in advance.  Your characters may surprise you as much as they do the players, and that's good.  That's real.

    <h4>⇀ Fill the characters' lives with barbed hooks</h4>
    Use the characters to lay hooks in your player's skin, then tug at the strings.  Pull them in different directions.  Drag them toward conflict.  Hold the line tight behind them and ask if they are willing to keep moving forward.  You have no threat of mortality to leverage at your players.  All you have are the people who mean something to them.

    <h4>⇀ Play to find out what happens</h4>
    Show up to the table with as little or as much preparation as you want (we suggest the former).  Readiness is not as important as flexibility.  Expect the players to surprise you.  Expect to surprise yourself sometimes.  The goal is to <em>discover a story through play</em>, not to tell a written tale.
