<h2 id="abyss">
    The Abyss
</h2>
Life has a way of accruing woe, especially the way that the characters in Deathless live it.  Depravity and affliction pool into a well that fills a person with still, bleak water, glassy in its self-reflection.  That accumulation, whether it collects to a pool or a sea, is the Abyss.  Call it horror or guilt or sin or anguish, if you want, it's all the same: the Abyss is weight, fatigue, waiting for its moment to drown someone in it.
<br><br>
Few understand the Abyss like the players' characters do.  Many sense it inside themselves: a perception with presence but no name or shape, try as they might to wrest it into being.  A couple even have an instinct for it in others, able to ferret out glimpses through eldritch or accidental methods.  But it isn't until death that one truly faces themselves with unblinking comprehension.  That is an experience the deathless alone know.

<h3 id="filling_the_abyss">
    Filling
</h3>
You take a point of Abyss under certain, often extereme circumstances.  Suffering the loss of your own life, for one.  You're a mortal construct and not designed to comprehend eternity.  Not even equipped to process the spasms of bodily failure.  Bearing witness to the sensation of a stuttering heartbeat, water-filled lungs, or metal through the belly is meant to occur once in a, well, lifetime.  People are not intended to revisit that trauma.  It will always leave its mark on you.
<br><br>
Taking another person's life will do the same.  Unlike dieing, people are sufficiently equipped for killing; to the point of creativity, even.  But capability is not comfort.  To reach out with intent and violence and snuff a human is to open yourself and invite the ghost of them inside of you.  Haunted is the best outcome you can hope for.  Pray they don't try to claw their way out of you again.
<br><br>
In between living and dieing, all manner of wrongs can have their affect.  Rather than detail all the possibilities, the game abides a simple principle: when you participate in, or allow to happen, an act that crosses an unbearable line, take a point of Abyss.  Where these lines lie is up to you, the player, at the moment that they happen.  Be honest with yourself.  The goal isn't to contrive scenes that causes you to recoil; there is no quota to achieve.  Simply, be attentive and aware, and when something crosses a line, make it known.

